#!/usr/bin/env node
import * as fs from "node:fs";
import * as path from "node:path";
import { fileURLToPath } from "node:url";
/**
 * Bundle the branding script for browser page.evaluate.
 * Output: src/lib/branding/branding-script.inject.ts
 */
import * as esbuild from "esbuild";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.join(__dirname, "..");
const entryPoint = path.join(
  rootDir,
  "src/scraper/scrapeURL/engines/fire-engine/branding-script/index.ts",
);
const outPath = path.join(
  rootDir,
  "src/lib/branding/branding-script.inject.ts",
);

const result = await esbuild.build({
  entryPoints: [entryPoint],
  bundle: true,
  minify: true,
  format: "iife",
  globalName: "__extractBrandDesign",
  target: ["es2020"],
  write: false,
});

const bundledCode = result.outputFiles[0].text;

const wrappedScript = `(function __extractBrandDesign() {
${bundledCode}
return typeof __extractBrandDesign !== "undefined" && typeof __extractBrandDesign.extractBrandDesign === "function"
  ? __extractBrandDesign.extractBrandDesign()
  : (typeof extractBrandDesign === "function" ? extractBrandDesign() : null);
})();`;

const escaped = JSON.stringify(wrappedScript);
const tsContent = `/**
 * Auto-generated by scripts/build-branding-script.mjs
 * Do not edit manually.
 */
export const BRANDING_SCRIPT: string = ${escaped};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, tsContent, "utf8");

console.log(`Wrote ${outPath} (${wrappedScript.length} chars)`);
